//堆与栈
/*
对比：
      对比                      堆                                          栈
    分配方式        系统自动分配(函数调用、局部变量)                  程序员手动分配(new/malloc)
    释放方式        系统自动释放(函数返回、作用域结束)          程序员手动释放(delete/free),忘记会内存泄漏
    生长方向           向下生长(高地址->低地址)                       向上生长(低地址->高地址)
    空间大小                固定、较小                         动态、极大(受物理内存/虚拟内存上限限制)
    分配效率         极快(仅移动栈指针，无额外开销)              较慢(需要内存管理器查找空闲块、合并碎片)
    碎片问题         无(自动连续分配、函数出栈即回收)           易产生内存碎片(频繁分配/释放导致空闲块不连续)
    溢出风险          栈溢出(无限递归、栈上超大数组)                   内存泄漏、野指针、堆溢出
    存储内容    函数调用帧、局部变量、返回地址、寄存器状态         动态分配的对象、大数组、全局/静态变量(部分)
堆溢出/栈溢出区别：
    栈溢出：栈空间耗尽，通常由无限递归、栈上大数组导致，直接触发段错误崩溃
    堆溢出：堆空间耗尽，通常由内存泄漏、超大堆分配导致，程序分配失败
为什么堆向上/栈向下生长：
    为了最大化利用内存空间，栈从高地址向下、堆从低地址向上，中间预留空闲内存，避免互相挤占
*/

#include <iostream>
using namespace std;

// 栈上变量：函数内局部变量，自动分配/释放
void stack_demo(){
    int a=10;//栈上分配
    int b=20;//栈上分配，地址比a低（栈向下生长）
    cout<<"栈变量a地址:"<<&a<<"，值:"<<a<<endl;
    cout<<"栈变量b地址:"<<&b<<"，值:"<<b<<endl;
    cout<<"b地址<a地址:"<<(&b<&a)<<"栈向下生长"<<endl;
}

int main(){
    cout<<"=====栈vs堆区别演示====="<<endl;

    //1.栈演示
    cout<<"\n【栈区域】"<<endl;
    stack_demo();

    //2.堆演示
    cout<<"\n【堆区域】"<<endl;
    int* p1=new int(1);//堆上分配
    int* p2=new int(2);//堆上分配，地址比p1高（堆向上生长）
    int* p3=new int(3);

    cout<<"堆变量p1地址:"<<p1<<"，值:"<<*p1<<endl;
    cout<<"堆变量p2地址:"<<p2<<"，值:"<<*p2<<endl;
    cout<<"堆变量p3地址:"<<p3<<"，值:"<<*p3<<endl;
    cout<<"p2地址>p1地址:"<<(p2>p1)<<"堆向上生长"<<endl;

    //3.必须手动释放堆内存，否则内存泄漏
    delete p1;
    delete p2;
    delete p3;

    return 0;
}